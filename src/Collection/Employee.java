package Collection;

public class Employee {
	int empId;
	String empPhone;
	String empPan;
	int getEmpId()
	{
		return empId;
	}
	void setEmpId(int empId)
	{
		this.empId=empId;
	}
	String getEmpPhone()
	{
		return empPhone;
	}
	void setEmpPhone(String empPhone)
	{
		this.empPhone=empPhone;
	}
	String getEmpPan()
	{
		return empPan;
	}
	void setEmpPan(String empPan)
	{
		this.empPan=empPan;
	}
}
